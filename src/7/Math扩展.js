/*
    去除小数部分
 */
console.log(Math.trunc(4.1));   // 4
console.log(Math.trunc(-4.1));  // -4
console.log("-----------------------------");
/*
    判断整数还是负数
 */
console.log(Math.sign(999));    // 1
console.log(Math.sign(-999));   // -1
console.log(Math.sign(9.1));    // 1
console.log(Math.sign(0));      // 0
console.log(Math.sign(-0));     // -1
